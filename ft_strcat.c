#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = s1;
	while (*ptr)
		ptr++;
	while (*s2)
		*ptr++ = *s2++;
	*ptr = '\0';
	return (s1);
}
